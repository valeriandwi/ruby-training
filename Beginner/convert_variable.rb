print("Variable Y\n")
y = 20
print(y)
# to_f function is used to convert data to float
print("\nConverted to Float \n")
print(y.to_f)

# to_s function is used to convert data to string
print("\nConverted to String \n")
print(y.to_s)

# to_s with parameter 2 is used to convert data to binary
print("\nConverted to Binary \n")
print(y.to_s(2))

# to_s with parameter 16 is used to convert data to hexadecimal
print("\nConverted to Hexadecimal\n")
print(y.to_s(16))

# to_s with parameter 8 is used to convert data to octal
print("\nConverted to Octal\n")
print(y.to_s(8))

print("\n")